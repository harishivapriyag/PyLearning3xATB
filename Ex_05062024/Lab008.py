# Dynamically Typed
# dynamic type -type of data that is supported by python [not known until runtime]
# Python is dynamically typed, meaning that the type of a variable is determined at runtime
# Example of dynamically typed language
x = 5
age = 25
# variable_name= variable_value
# identifier = literal
# age---variable
# =--assignment operator
# 25--value
print(x)
print(age)
print(type(age))
age = "twenty five"
print(age)
print(type(age))
# datatypes supported by python
# int,float,str,bool
# int
# 1, -1, 2000, 243435
a = 7
print(a)
print(type(x))  # Output: <class 'int'>
# float
# -3.93453, 5.325, 1.0, 52.3,0.334
y = 3.14
print(y)
print(type(y))  # Output: <class 'float'>
# str
# "Hi", "I am beautiful", "Hari Shivapriya", "123", "2.5", "h"
name = "Harini"
print(name)
print(type(name))  # Output: <class 'str'>
# bool
# True, False
is_student = True
print(type(is_student))  # Output: <class 'bool'>
# how to check the type of variable
# type(variable_name)
# complex number -i - iota
complex_number = 3 + 4j
print(complex_number)
print(complex_number.imag)
print(complex_number.real)
print(type(complex_number))
