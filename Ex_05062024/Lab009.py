# Dynamic Typing
# Python is a dynamically typed language, which means that the type of a variable is determined at runtime. This means that you don't need to declare a variable's type before using it in the program.

# Example
# Here's an example of how you can use a variable in Python without declaring its type:

age = 25
print(age)
print(type(age))
age = "twenty five"
print(age)
print(type(age))
age = "ab1"
print(age)
print(type(age))
