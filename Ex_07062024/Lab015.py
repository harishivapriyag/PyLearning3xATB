# List
a = [1, 2, 3, 4, 5]
# Print the list
print(a)
# Print the type of the list
print(type(a))
# Accessing elements of the list
print(a[0])
# Slicing the list
print(a[1:3])
# Adding elements to the list
a.append(6)
#  Removing elements from the list
a.remove(3)
print(a)
# Sorting the list
a.sort()
# Taken input from the user
