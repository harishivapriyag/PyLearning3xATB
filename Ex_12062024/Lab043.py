# Loops
# Repeated steps of actions to perform the same set of action
# for loop
# while loop
# nested loop

# for loop
# print numbers from 1 to 10
for i in range(1, 11):
    print(i)
for i in range(5):
    print(i)
for harini in range(7, 15):
    print(harini)
# range (start, stop, step) # start is inclusive and stop is exclusive
for i in range(1, 10, 2):
    print(i)
