# Literals in python
# var_name = variable_value

name = "Hari"  # String Literals
# var_name --> Identifier
# variable_value --> Literal
# "Hari" --> String Literal

# Literals are the values that are assigned to variables.
# Literals are the actual values that are assigned to variables.
# Literals can be numeric and non-numeric

# String Literals
# String literals are a sequence of characters enclosed in single or double quotes.
name = "Hari"
print(name)
# Numeric Literals
# Numeric literals are numbers that can be written without any quotes.
# Numeric literals can be integers, floats, or complex numbers.

# Integer Literals
# Integer literals are numbers without a decimal point.
age = 25
print(age)
# Float Literals
# Float literals are numbers with a decimal point.
height = 5.6
print(height)
# Complex Literals
# Complex literals are numbers with a real and imaginary part.
complex_num = 3 + 4j
print(complex_num)
# Boolean Literals
# Boolean literals are either True or False.
is_student = True
print(is_student)
# None Literals
# None literals are the null values in Python.
# None is a special value that represents the absence of a value.
value = None
print(type(value))
# Literal Collections
# Literal collections are the collections of literals.
# Literal collections can be lists, tuples, sets, or dictionaries.
# List Literals
# List literals are the sequences of values enclosed in square brackets.
list_values = [1, 2, 3, 4, 5]
print(list_values)
# Tuple Literals
# Tuple literals are the sequences of values enclosed in parentheses.
tuple_values = (1, 2, 3, 4, 5)
print(tuple_values)
# Set Literals
# Set literals are the sequences of values enclosed in curly braces.
set_values = {1, 2, 3, 4, 5}
print(set_values)
# Dictionary Literals
# Dictionary literals are the sequences of key-value pairs enclosed in curly braces.
dict_values = {1: "one", 2: "two", 3: "three"}
print(dict_values)
# Variable Naming Conventions
# Variable naming conventions are the rules and guidelines for naming variables in Python.
# Variable names should follow the naming conventions below:
# 1. Variable names should start with a letter or an underscore.
# 2. Variable names should contain only letters, digits, and underscores.
# 3. Variable names are case-sensitive.
# 4. Variable names should not be a reserved keyword in Python.
# 5. Variable names should not contain spaces.
# 6. Variable names should not start with a digit.
# 7. Variable names should not be a built-in function name.
# 8. Variable names should not contain special characters except underscore.
# 9. Variable names should be descriptive and meaningful.
# 10. Variable names should be concise and not too long.
# 11. Variable names should be consistent within a project or application.
# 12. Variable names should be clear and not too ambiguous.
